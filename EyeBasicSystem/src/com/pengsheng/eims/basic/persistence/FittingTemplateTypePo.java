package com.pengsheng.eims.basic.persistence;

public class FittingTemplateTypePo {

	private String bfttid;           // 主键
	private String bfttname;         // 报表类型名称
	private String bftid;         	 // 该类型配置的报表模版ID
	private String bftserver;        // 该类型配置的报表模版Server类型(1:Finereport;2:ReportService)
	private String bfttshowtype;	 // 打开方式;1:层显示;2:弹窗显示;
	
	private String bftfilename = null;     // 文件名称
	private String bftfileurl = null;      // 文件路径
	private String bftname  = null;  // 样式名称
	
	public String getBfttid() {
		return bfttid;
	}
	public void setBfttid(String bfttid) {
		this.bfttid = bfttid;
	}
	public String getBfttname() {
		return bfttname;
	}
	public void setBfttname(String bfttname) {
		this.bfttname = bfttname;
	}
	public String getBftid() {
		return bftid;
	}
	public void setBftid(String bftid) {
		this.bftid = bftid;
	}
	public String getBftserver() {
		return bftserver;
	}
	public void setBftserver(String bftserver) {
		this.bftserver = bftserver;
	}
	public String getBftfilename() {
		return bftfilename;
	}
	public void setBftfilename(String bftfilename) {
		this.bftfilename = bftfilename;
	}
	public String getBftfileurl() {
		return bftfileurl;
	}
	public void setBftfileurl(String bftfileurl) {
		this.bftfileurl = bftfileurl;
	}
	public String getBftname() {
		return bftname;
	}
	public void setBftname(String bftname) {
		this.bftname = bftname;
	}
	public String getBfttshowtype() {
		return bfttshowtype;
	}
	public void setBfttshowtype(String bfttshowtype) {
		this.bfttshowtype = bfttshowtype;
	}
}
