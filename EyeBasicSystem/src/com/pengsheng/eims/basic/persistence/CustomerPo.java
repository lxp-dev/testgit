package com.pengsheng.eims.basic.persistence;

public class CustomerPo {

	private String bctid;	//客户ID
	private String bctcustomername;	//客户名称
	private String bctcontactperson;	//联系人
	private String bctcontactphone;	//电话
	private String bctfax;	//传真
	private String bctaddress;	//地址
	private String bctremark;	//备注
	public String getBctid() {
		return bctid;
	}
	public void setBctid(String bctid) {
		this.bctid = bctid;
	}
	public String getBctcustomername() {
		return bctcustomername;
	}
	public void setBctcustomername(String bctcustomername) {
		this.bctcustomername = bctcustomername;
	}
	public String getBctcontactperson() {
		return bctcontactperson;
	}
	public void setBctcontactperson(String bctcontactperson) {
		this.bctcontactperson = bctcontactperson;
	}
	public String getBctcontactphone() {
		return bctcontactphone;
	}
	public void setBctcontactphone(String bctcontactphone) {
		this.bctcontactphone = bctcontactphone;
	}
	public String getBctfax() {
		return bctfax;
	}
	public void setBctfax(String bctfax) {
		this.bctfax = bctfax;
	}
	public String getBctaddress() {
		return bctaddress;
	}
	public void setBctaddress(String bctaddress) {
		this.bctaddress = bctaddress;
	}
	public String getBctremark() {
		return bctremark;
	}
	public void setBctremark(String bctremark) {
		this.bctremark = bctremark;
	}

}
