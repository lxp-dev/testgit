package com.pengsheng.eims.basic.persistence;

public class PhotoPo {

	private String id;

	private String faqID;

	private String fileName;

	private String saveFileName;

	private String saveFileLittleName;

	public String getSaveFileLittleName() {
		return saveFileLittleName;
	}

	public void setSaveFileLittleName(String saveFileLittleName) {
		this.saveFileLittleName = saveFileLittleName;
	}

	private String contentType;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getFaqID() {
		return faqID;
	}

	public void setFaqID(String faqID) {
		this.faqID = faqID;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getSaveFileName() {
		return saveFileName;
	}

	public void setSaveFileName(String saveFileName) {
		this.saveFileName = saveFileName;
	}

	public String getContentType() {
		return contentType;
	}

	public void setContentType(String contentType) {
		this.contentType = contentType;
	}


}
