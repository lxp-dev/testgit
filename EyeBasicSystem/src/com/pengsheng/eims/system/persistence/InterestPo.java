package com.pengsheng.eims.system.persistence;

/*
 * 兴趣爱好Po
 */
public class InterestPo {
	
	//兴趣爱好编码
	private String birid;
	
	//兴趣爱好名称
	private String birname;
	
	private String flag;

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	public String getBirid() {
		return birid;
	}

	public void setBirid(String birid) {
		this.birid = birid;
	}

	public String getBirname() {
		return birname;
	}

	public void setBirname(String birname) {
		this.birname = birname;
	}

}
